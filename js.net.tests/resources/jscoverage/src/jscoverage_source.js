goog.provide('jsnet.jscoverage.source');

jsnet.jscoverage.source.add = function(x, y) { return x + y };

jsnet.jscoverage.source.subtract = function(x, y) { return x - y };

jsnet.jscoverage.source.multiply = function(x, y) { return x * y };

jsnet.jscoverage.source.divide = function(x, y) { return x / y };